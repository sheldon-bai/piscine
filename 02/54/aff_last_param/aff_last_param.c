#include <unistd.h>

int		main(int argc, char **argv)
{
	int		arg;
	int		i;

	i = 0;
	arg = argc - 1;
	if (argc < 2)
		return (0);
	while (argv[arg][i])
		write(1, &argv[arg][i++], 1);
	write(1, "\n", 1);
	return (0);
}
