char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (j < i)
	{
		c = str[j];
		str[j] = str[i - 1];
		str[i - 1] = c;
		i--;
		j++;
	}
	return (str);
}
