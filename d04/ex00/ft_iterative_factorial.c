int		ft_iterative_factorial(int nb)
{
	int		fac;

	fac = 1;
	if (nb < 0 || nb >= 13)
		return (0);
	else if (nb == 0 || nb == 1)
		return (1);
	while (nb > 1)
	{
		fac = fac * nb;
		nb--;
	}
	return (fac);
}
