int		ft_is_prime(int nb)
{
	int		i;

	i = 2;
	if (nb <= 1)
	{
		return (0);
	}
	else if (nb == 2 || nb == 3)
		return (1);
	else if (nb == 4)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
